    template <typename T>
    concept aggregate = std::is_aggregate_v<std::decay_t<T>>;

    struct wildcard {
        template <typename T>
            requires(!std::is_lvalue_reference_v<T>)
        operator T &&() const;

        template <std::copy_constructible T>
        operator T &() const;
    };

    template <aggregate structure, typename index_sequence = std::index_sequence<>, typename = void>
    struct arity_counter : index_sequence {};

    template <aggregate structure, std::size_t... indices>
    struct arity_counter<
        structure,
        std::index_sequence<indices...>,
        std::void_t<decltype(structure{(void(indices), std::declval<wildcard>())..., std::declval<wildcard>()})>>
        : arity_counter<structure, std::index_sequence<indices..., sizeof...(indices)>> {};

    template <aggregate T>
    constexpr auto arity = arity_counter<std::decay_t<T>>::size();

